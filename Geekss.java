class Geekss {
    static int add(int a, int b) {
        return a + b;
    }

    static int add(int a, int b, int c) {
        return a + b + c;
    }

    public static void main(String[] args) {
        System.out.println("add() with 2 parameters: " + add(4, 6));
        System.out.println("add() with 3 parameters: " + add(9, 6, 7));
    }
}

